/* USER CODE BEGIN 4 */
int subTimer = 1;
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef * htim) { //function called at 8Hz
	  if(led_color == 'G'){
		  if(subTimer == 4) { //Green LED toggles at 2Hz, or once every 4 interrupts
			  HAL_GPIO_TogglePin(GPIOB, GPIO_PIN_0);
			  subTimer = 1;
		  } else {
			  subTimer++;
		  }
	  } else if (led_color == 'Y') { //Yellow LED toggles at 4Hz, or once every 2 interrupts
		  if(subTimer == 2) {
			  //HAL_GPIO_TogglePin(GPIOE, GPIO_PIN_1);
			  GPIOE->ODR ^= 0b10; //Yellow LED is directly toggled in register to meet req
			  subTimer = 1;
		  } else {
			  subTimer++;
		  }
	  } else if (led_color == 'R') { //Red LED toggles at 8Hz, or every interrupt
		  HAL_GPIO_TogglePin(GPIOB, GPIO_PIN_14);
	  }
}

void HAL_GPIO_EXTI_Callback (uint16_t GPIO_Pin) { //Called when button is released (falling edge)
	if(GPIO_Pin == GPIO_PIN_13) {
		HAL_GPIO_WritePin(GPIOB, GPIO_PIN_0, GPIO_PIN_RESET);
		HAL_GPIO_WritePin(GPIOE, GPIO_PIN_1, GPIO_PIN_RESET);
		HAL_GPIO_WritePin(GPIOB, GPIO_PIN_14, GPIO_PIN_RESET); //turn off all LEDs and reset timer
		subTimer = 1;
		if(led_color == 'G') { //swap LED color sequentially
			led_color = 'Y';
		} else if(led_color == 'Y') {
			led_color = 'R';
		} else if(led_color == 'R') {
			led_color = 'G';
		}
	}
}


